#include "libft.h"

int	ft_strcmp(char *s1, char *s2, int len)
{
	int	i;

	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && i < len)
	{
		if ((int)s1[i] > (int)s2[i])
			return (1);
		if ((int)s1[i] < (int)s2[i])
			return (-1);
		i++;
	}
	return (0);
}
